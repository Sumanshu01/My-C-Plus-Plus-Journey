#include <iostream>
#include <string.h>
using namespace std;

int main() {
   
//double quote in a string
    string txt1 = "We are the so-called \"Vikings\" from the north.";
    cout << txt1 <<"\n";
// single quote in a string
    string txt2 = "It\'s alright.";
    cout << txt2 <<"\n";
// single backslash in a string
    string txt3 = "The character \\ is called backslash.";
    cout << txt3 <<"\n";

    return 0;
}